import turtle
t=turtle.Turtle()
t.penup()
t.goto(-200, -80)
t.pendown()
t.color("saddlebrown")
t.begin_fill()
t.right(45)
t.forward(100)
t.left(45)
t.forward(250)
t.left(45)
t.forward(100)
t.end_fill()

t.penup()
t.goto(0,-80)
t.pendown()
t.color("black")
t.begin_fill()
t.left(45)
for i in range(2):
  t.forward(200)
  t.left(90)
  t.forward(10)
  t.left(90)
t.end_fill()

t.penup()
t.forward(14)
t.pendown()
t.color("black")
t.forward(180)
